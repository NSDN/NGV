int main(void)
{

  /* USER CODE BEGIN 1 */
	lcd = LCDInit(&rd, &wr, &rs, &cs, &rst, data);
	flash = FlashInit(&hspi6, FLASH_CS_GPIO_Port, FLASH_CS_Pin, W25Q64);
  /* USER CODE END 1 */

  /* Enable I-Cache-------------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache-------------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI6_Init();
  MX_USART2_UART_Init();
  MX_FATFS_Init();
  MX_DMA2D_Init();
  MX_SDMMC1_SD_Init();
  MX_LWIP_Init();
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 2 */
	setjmp(rstPos);
	USBD_Stop(&hUsbDeviceFS);

  	lcd->init(lcd->p);
  	lcd->rotate(lcd->p, LCD_LANDSCAPE);
  	lcd->font(lcd->p, Small);
  	lcd->colorb(lcd->p, 0xFFFFFF);
  	lcd->colorf(lcd->p, 0x000000);
  	lcd->clear(lcd->p);

  	lcd->bitmapsc(lcd->p, lcd->p->width / 2, 140, 64, 64, __NYAGAME_LOGO_);
  	lcd->printfc(lcd->p, 180, "nyagame vita");
  	HAL_Delay(1000);
  	lcd->clear(lcd->p);

  	lcd->printfa(lcd->p, "NyaGame Vita Factory System\n");
  	lcd->printfa(lcd->p, "Version: %s\n\n", NGV_CORE_VERSION);

  	/* Initialize device */
  	uint8_t result = 0;
  	if (HAL_GPIO_ReadPin(SD_STATE_GPIO_Port, SD_STATE_Pin) == GPIO_PIN_RESET) {
  		lcd->printfa(lcd->p, "Init SD card... OK\n");
		HAL_SD_Init(&hsd1, &SDCardInfo1);
	} else {
		lcd->printfa(lcd->p, "Init SD card... ERR\n");
		HAL_SD_DeInit(&hsd1);
	}
  	lcd->printfa(lcd->p, "Mount SD card...\n");
  	f_mount(&fileSystem, SD_Path, 1);
  		result = f_mount(&fileSystem, SD_Path, 1);
  		if(result == FR_OK) {
  			char path[] = "NGV_INFO.TXT";
  			FIL boardInfo;
  			f_open(&boardInfo, path, FA_WRITE | FA_CREATE_ALWAYS);
  			f_printf(&boardInfo, "NyaGame Vita Factory Edition with STM32F767\n");
  			f_printf(&boardInfo, "by NyaSama Developer Network\n");
  			f_printf(&boardInfo, "Firmware Version: %s\n", NGV_CORE_VERSION);
  			f_close(&boardInfo);
  			lcd->printfa(lcd->p, "Test SD card... OK\n");
  			FS_OK = 1;
  		} else {
  			lcd->printfa(lcd->p, "Test SD card... ERR: %02X\n", result);
  			FS_OK = 0;
  		}

	lcd->printfa(lcd->p, "Init USB Mass Storage...\n");
	USBD_Start(&hUsbDeviceFS);

  	result = 0;
  	result = flash->begin(flash->p);
	lcd->printfa(lcd->p, "Init flash... %s\n", result ? "OK" : "ERR");
	/* Initialize end */

	lcd->printfa(lcd->p, "\n");
	char* args[2] = {
			"nshel",
			"init.d"
	};
	nshel(2, args);
  	lcd->printfa(lcd->p, "\n");

  	HAL_Delay(500);

  	lcd->printfa(lcd->p, "Push blue button to flash.\n\n");
  	while (HAL_GPIO_ReadPin(User_Blue_Button_GPIO_Port, User_Blue_Button_Pin) != GPIO_PIN_SET);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1) {
		HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_SET);
		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
		lcd->colorb(lcd->p, 0xFF0000);
		lcd->clear(lcd->p);
		HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
		lcd->colorb(lcd->p, 0x00FF00);
		lcd->clear(lcd->p);
		HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
		lcd->colorb(lcd->p, 0x0000FF);
		lcd->clear(lcd->p);
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */

}

